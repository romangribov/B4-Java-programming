package io.loop.test.day06_b_unary_operators;

public class IncrementExample2 {
    public static void main(String[] args) {

        int age = 30;
        System.out.println(age); //30
        System.out.println(age++); // POST -increment -- >  30 -- > update - x = x + 1  -- > x = 31
        System.out.println(age);

        System.out.println(age++); // POST-INCREMENT -- > PRINT 31 -- > update > x = x +1; -- > x = 32
        System.out.println(age++); // POST-INCREMNT





    }


}
